defmodule Solution.Day6.GridStringTest do
  use ExUnit.Case
  alias Solution.Day6.GridString

  test "From String" do
    assert GridString.from_string("""
           |   |   | 2 |   |   |   |   |   |   |
           |   | 2 | 1 | 2 |   |   |   |   |   |
           | 2 | 1 | 0 | 1 | 2 |   |   |   |   |
           |   | 2 | 1 | 2 |   |   | 2 |   |   |
           |   |   | 2 |   |   | 2 | 1 | 2 |   |
           |   |   |   |   | 2 | 1 | 0 | 1 | 2 |
           |   |   |   |   |   | 2 | 1 | 2 |   |
           |   |   |   |   |   |   | 2 |   |   |
           """) == %GridString{grid: [
               [" ", " ", "2", " ", " ", " ", " ", " ", " "],
               [" ", "2", "1", "2", " ", " ", " ", " ", " "],
               ["2", "1", "0", "1", "2", " ", " ", " ", " "],
               [" ", "2", "1", "2", " ", " ", "2", " ", " "],
               [" ", " ", "2", " ", " ", "2", "1", "2", " "],
               [" ", " ", " ", " ", "2", "1", "0", "1", "2"],
               [" ", " ", " ", " ", " ", "2", "1", "2", " "],
               [" ", " ", " ", " ", " ", " ", "2", " ", " "]
             ]}
  end
end
